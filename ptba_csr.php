	<th><h3 class="big">Corporate Social Responsibility</h3></th>
	<tr><td><div  >Pelaksanaan tanggung jawab perusahaan terintegrasi dalam “Pedoman CSR PTBA” yang mencakup enam kegiatan, yaitu bidang : (1) ekonomi, (2) lingkungan, (3) hak azasi manusia, (4) praktik ketenagakerjaan, dan (5) kelaikan kerja, tanggung jawab produk, dan (6) kemasyarakatan.
Keenam focus kegiatan tersebut mengacu kepada kaidah internasional mengenai keberhasilan implementasi CSR yang ditetapkan oleh Global Reporting Initiatives (GRI), dan dilandasi oleh etika/norma bisnis yang berlaku. Meliputi pembangunan ekonomi dan pemberdayaan masyarakat lokal, peduli terhadap pelestarian lingkungan hidup, jaminan pelaksanaan non diskriminasi dan penghargaan hak azasi manusia, jaminan kesehatan dan keselamatan kerja serta upaya peningkatan kesejahteraan para karyawan, jaminan keamanan penggunaan produk dan kepuasaan pelanggan, menjalin hubungan harmonis dengan masyarakat.</div>
<div  >&nbsp;</div>
<div  >Perseroan telah melaksanakan kegiatan dibidang sosial, ekonomi dan lingkungan agar hasil kegiatan operasional dari sisi kinerja ekonomi, sosial dan lingkungan seimbang. Hal tersebut juga sebagai bentuk Tanggung Jawab Sosial dan Lingkungan terhadap masyarakat sekitar perusahaan agar meningkatkan taraf hidup masyarakat maupun pelestarian lingkungan.</div>
<div  >&nbsp;</div>
<div  >Melalui program kemitraan dan bina lingkungan (PKBL) serta bina wilayah, Perseroan mengadakan kegiatan yang bertujuan memberdayakan potensi sosial ekonomi dan penciptaan kualitas hidup yang lebih baik untuk masyarakat dan lingkungan sekitar. Pelaksanaan PKBL dan program bina wilayah tersebut berpedoman pada Peraturan Menteri Negara Badan Usaha Milik Negara (BUMN) dan Undang-Undang.</div>
<div  >&nbsp;</div>
<div  ><strong>PENINGKATAN PROGRAM KEMITRAAN DAN BINA LINGKUNGAN</strong></div>
<div  >&nbsp;</div>
<div  >Tujuan Program Kemitraan PTBA adalah peningkatan kemampuan usaha kecil dan koperasi di sekitar wilayah operasi Perseroan agar tangguh dan mandiri dengan pemanfaatan dana dari sebagian laba perseroan. Kegiatan Bina Lingkungan sendiri bertujuan untuk pemberdayaan program sosial kemasyarakatan.</div>
<div  >&nbsp;</div>
<div  >Perseroan bertekad meningkatkan kuantitas dan kualitas pelaksanaan Program Kemitraan maupun Bina Lingkungan. Untuk Program Kemitraan, Perseroan menargetkan peningkatan kemandirian mitra binaan dan membantu perluasan penjualan produk mitra binaan di wilayah operasional Perseroan. Kerja sama penyaluran dana PK maupun BL dengan beberapa pihak yang kompeten dilakukan untuk peningkatan kualitas mitra binaan.</div>
<div  >&nbsp;</div>
<div  >Pada tahun 2011 Perseroan semakin aktif mengajak dan melibatkan peran-serta masyarakat secara langsung dalam proses perencanaan, pelaksanaan maupun monitoring program pemberdayaan sosial ekonomi masyarakat di lingkar tambang, sehingga pembangunannya tepat sasaran dan sesuai kebutuhan masyarakat. Perseroan juga menjadikan pelaksanaan kegiatan Bina Lingkungan bidang pendidikan menjadi prioritas. Melalui program PKBL dan Bina Wilayah, Perseroan meyakini tumbuhnya kesejahteraan sosial dan ekonomi masyarakat sekitar menjadi lebih berdaya dan lebih mandiri.</div>
<div  >&nbsp;</div>
<div  >Penyaluran total dana PKBL tahun 2011 yang dialirkan oleh Perseroan naik 55,4% dari tahun 2010, dari sebesar Rp 93,42 miliar menjadi Rp 145,20 miliar.</div>
<div  >&nbsp;</div>
<div  ><strong>PROGRAM KEMITRAAN</strong></div>
<div  >&nbsp;</div>
<div  >Perseroan terus meningkatkan kemandirian mitra binaan sekaligus membantu memperluas penjualan produk mitra binaan. Penyaluran Dana Kemitraan dilakukan secara selektif dengan mempertimbangkan kondisi yang dimiliki oleh calon mitra binaan.</div>
<div  >&nbsp;</div>
<div  >Jenis komoditas calon mitra binaan yang diprioritaskan untuk  mendapatkan bantuan pembinaan meliputi komoditas yang menjadi andalan daerah, komoditas tradisional yang potensial untuk dikembangkan, komoditas yang berpeluang ekspor, komoditas yang menyerap tenaga kerja.</div>
<div  >&nbsp;</div>
<div  >Pada tahun 2011, Perseroan telah merealisasikan dana Program Kemitraan sebesar Rp 98,95 miliar. Dana yang disalurkan tersebut meliputi dana pinjaman lunak kepada Usaha Kecil Menengah sebesar Rp 11,62 miliar, kerjasama dengan BUMN Penyalur sebanyak 6 (enam) perusahaan sebesar Rp 84,81 miliar, dan dana pembinaan sebesar Rp 2,51 miliar. Penyaluran dana Program Kemitraan yang direalisasikan pada tahun 2011 meningkat 45,9% dari realisasi tahun 2010 sebesar Rp 67,63 miliar.</div>
<div  >&nbsp;</div>
<div  >Efektivitas penyaluran dana Program Kemitraan tahun 2011 sebesar 85%, sedangkan tingkat kolektibilitas pengembalian pinjaman mencapai 79%. Dana pinjaman lunak tersebut disalurkan kepada 473 (empat ratus tujuh puluh tiga) mitra binaan/Usaha kecil dan koperasi yang tersebar di 5 (lima) wilayah. Perseroan juga akan meningkatan upaya sinergi dan profesionalitas dalam kegiatan penyaluran dan berupaya meningkatkan tingkat kolektibilitas pengembalian pinjaman dana Program Kemitraan.</div>
<div  >&nbsp;</div>
<div  ><strong>PROGRAM BINA LINGKUNGAN</strong></div>
<div  >&nbsp;</div>
<div  >Program Bina Lingkungan PTBA dielaborasi dalam enam fokus kegiatan, yaitu Program Pendidikan, Program Pengembangan Prasarana dan Sarana Umum, Program Perbaikan Sarana Ibadah, Program Peningkatan Kesehatan, Program Pelestarian Alam dan Program Bantuan Bencana. Tujuan dari program tersebut adalah untuk peningkatan standar hidup dan kesejahteraan masyarakat.</div>
<div  >&nbsp;</div>
<div  >Total dana yang disalurkan melalui pelaksanaan Program Bina lingkungan meningkat sebesar 76,9% dari Rp 25,7 miliar di tahun 2010 menjadi Rp 45,3 miliar di tahun 2011. Semua bantuan tersebut disalurkan melalui empat wilayah kerja mencakup Unit pertambangan Tanjung Enim, Unit Pertambangan Ombilin, Pelabuhan Tarahan dan Dermaga Kertapati.</div>
<div  >&nbsp;</div>
<div  ><strong>PROGRAM BINA WILAYAH</strong></div>
<div  >&nbsp;</div>
<div  >Program Bina Wilayah bertujuan untuk memberdayakan potensi ekonomi masyarakat sekaligus mewujudkan komitmen Perseroan untuk bersama-sama menciptakan kualitas hidup yang lebih baik bagi masyarakat. Program ini merupakan pemberian bantuan berupa bantuan fisik maupun non-fisik dengan jangkauan wilayang yang lebih luas.</div>
<div  >&nbsp;</div>
<div  >Pada tahun 2011, pelaksanaan program tersebut banyak digunakan untuk kepentingan masyarakat. Partisipasi pembangunan tersebut disalurkan dalam bentuk dana Peran Serta Pembangunan Daerah. Tahun 2011 Perseroan menyalurkan dana Peran Serta kepada Pemprov Sumsel, Lampung, Pem Kab Muara Enim dan Lahat sebesar total Rp 38,6 miliar meningkat 125,4% dari tahun sebelumnya</div>
<div  >&nbsp;</div>
<div  >Perseroan bertasipasi dibidang olahraga melalui penyelesaian pembangunan sarana olah raga berupa gedung tenis dalam rangka penyelenggaraan SEA GAMES di Palembang. Selain itu Perseroan juga berpartisipasi dalam penyaluran dana pengembangan kegiatan olahraga ditingkat nasional maupun lokal.</div>
<div  >&nbsp;</div>
<div  >Di tahun 2011, total dana yang disalurkan melalui Program Bina Wilayah mencapai Rp 74,09 miliar, naik 229,5% dari nilai sebesar Rp 22,49 miliar di tahun 2010.</div>
<div  >&nbsp;</div>
<div  ><strong>PENGELOLAAN PELESTARIAN LINGKUNGAN</strong></div>
<div  >&nbsp;</div>
<div  >Misi Perseroan dalam bidang ini diwujudkan melalui penerapan program-program pengelolaan, pemantauan, pengembangan dan rehabilitasi lingkungan secara berkelanjutan.</div>
<div  >&nbsp;</div>
<div  >Pengelolaan Lingkungan Dengan Sistem Terakreditasi
Perseroan menjalankan sistem terakreditasi ISO 14001: 2004 untuk meningkatkan efektifitas kegiatan pengelolaan lingkungan yang mencakup sistem manajemen lingkungan, audit lingkungan, evaluasi kinerja lingkungan dan kajian daur hidup pokok. </div>
<div  >&nbsp;</div>
<div  >Penelitian dan Pengembangan Lingkungan</div>
<div  >Beberapa kegiatan penelitian dan pengembangan bidang lingkungan yang dilaksanakan pada tahun 2011 adalah Perseroan mengembangkan mikoriza Arbuskula dalam kegiatan pembibitan tanaman dan telah melakukan produksi massal, Perseroan mengembangkan teknik Kultur Jaringan, pemanfaatan Oli bekas untuk peledakan, melakukan revegetasi dengan tanaman sawit, ujicoba revegetasi secara lansung, serta melakukan konservasi tanaman lokal.</div>
<div  >&nbsp;</div>
<div  >Selain itu, Perseroan juga mengimplementasikan pola Green Mining dan sosialisasi lingkungan</div>
<div  >&nbsp;</div>
<div  >Laporan Pelaksanaan Reklamasi dan Rehabilitasi</div>
<div  >Pembukaan lahan dan proses reklamasi areal tambang Perseroan telah dilaksanakan sesuai dengan butir-butir ketentuan pada UU No. 4 Tahun 2009 dan Permen No 18 tahun 2008. Seluruh ketentuan tersebut telah dipenuhi oleh Perseroan.</div>
<div  >&nbsp;</div>
<div  >Perseroan melaksanakan program pembangunan Taman Hutan Raya / Tahura Enim dengan membagi area pasca tambang menjadi 3 blok dan 12 zona, yaitu :</div>
<ol>
<li>Blok Perlindungan (766,40 Ha)</li>
<li>Blok Koleksi Tanaman (2.973,14 Ha)</li>
<li>3. Blok Pemanfaatan (1.655,03 Ha)</li>
</ol>
<div  >Sedangkan pembagian zona adalah sbb :</div>
<ol>
<li>Zona Penerima/Rekreasi</li>
<li>Zona Sarana Prasarana</li>
<li>Zona Hutan Tanaman</li>
<li>Zona Kebun Koleksi</li>
<li>Zona Kebun Buah</li>
<li>Zona Peternakan</li>
<li>Zona Wisata Air</li>
<li>Zona Penelitian Produktif</li>
<li>Zona Pertanian/Agroforestry</li>
<li>Zona Perikanan</li>
<li>Zona Bumi Perkemahan</li>
<li>Zona Satwa</li>
</ol>
<div  >Tahun 2011, program reklamasi lahan pasca tambang sebagai Tahura Enim yang telah dilaksanakan adalah :</div>
<ul>
<li>Pembuatan laboratorium Kultur Jaringan,</li>
<li>Proses pelaksanaan relokasi penduduk dan TPU,</li>
<li>Menyelesaikan pembangunan Gedung olah raga, sarana olah raga Bowling, jogging track dan Futsal,</li>
<li>Melanjutkan rencana pembuatan kantor terpadu untuk Satker K3 dan BWE System,</li>
<li>Melakukan kerjasama penelitian lapangan lokal dengan Universitas Bengkulu,</li>
<li>Melakukan kerjasama penelitian jenis-jenis tanaman jarak dengan Universitas Sriwijaya   di IUP Banko Barat,</li>
<li>Melakukan pengkayaan tanaman dengan jenis tanaman lokal yang bernilai ekonomis  tinggi dan</li>
<li>Melakukan review master plan TAHURA ENIM.</li>
</ul>

<div  ><strong>PEMENUHAN HAK-HAK KARYAWAN DAN PENGHARGAAN TERHADAP HAK AZASI MANUSIA</strong></div>
<div  >&nbsp;</div>
<div  >Perseroan menyadari pentingnya untuk menciptakan hubungan kerja sama yang serasi antara manajemen dan seluruh karyawan Perseroan. Oleh karena itu, dalam praktiknya Perseroan memperlakukan hal yang sama terhadap semua karyawan dengan tidak memandang suku,  ras, agama, jender dan haluan politiknya, begitu juga karyawan memiliki kebebasan berserikat dan melaksanakan Perjanjian Kerja Bersama.</div>
<div  >&nbsp;</div>
<div  ><strong>KESEHATAN DAN KESELAMATAN KERJA SERTA LINGKUNGAN</strong></div>
<div  >&nbsp;</div>
<div  >Untuk mecapai standar kesehatan dan keselamatan kerja serta lingkungan yang tinggi, Perseroan menerapkan kebijakan serta penyediaan sarana dan prasarana untuk setiap karyawan. Dalam implementasi K3, Perseroan telah memperoleh sertifikasi Sistem Manajemen K3 (SMK3) dari Depnakertrans RI.</div>
<div  >&nbsp;</div>
<div  ><strong>KOMITMEN TERHADAP KUALITAS PRODUK DAN PERLINDUNGAN PELANGGAN</strong></div>
<div  >&nbsp;</div>
<div  >Standar kualitas dan perlindungan konsumen terhadap setiap produk yang dihasilkan mempunyai pengaruh yang signifikan bagi pertumbuhan kinerja usaha secara berkelanjutan. Oleh karena itu, Perseroan menetapkan dan memberlakukan kriteria  yang ketat dalam proses dan output produksi maupun pengawasan kualitas setiap produknya.</div>
<div  >&nbsp;</div>
<div  ><strong>HUBUNGAN HARMONIS DENGAN MASYARAKAT BERLANDASKAN PRINSIP-PRINSIP TATA KELOLA YANG BAIK</strong></div>
<div  >&nbsp;</div>
<div  >Melalui Program Kemitraan dan Bina Lingkungan (PKBL), serta program Bina Wilayah, Perseroan secara sistematis telah melaksanakan serangkaian kegiatan dengan melibatkan masyarakat. Sesuai dengan prinsip transparansi, Perseroan juga membuka akses dan menjalin komunikasi timbal balik dengan masyarakat dan pihak-pihak yang terkait.</div>
<div  >&nbsp;</div>
<div  ><strong>LAPORAN BERKELANJUTAN</strong></div>
<div  >&nbsp;</div>
<div  >Perseroan menerbitkan Laporan keberlajutan (Sustainability Report) yang merupakan pertanggungjawaban menyeluruh bagi seluruh pemangku kepentingan Perseroan mengenai pengelolaan dan realisasi program CSR.</div>

